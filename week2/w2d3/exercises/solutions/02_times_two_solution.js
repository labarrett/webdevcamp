/*
  Write a function that given an array, returns another array with each of the numbers multiplied by two.
  Don't change the original array, make sure you make a new array and return that new array.
*/

function arrayTimesTwo(nums){
  var twiceNums = [];
  for(var i = 0; i < nums.length; i++) {
      twiceNums.push(nums[i] * 2);
  }
  return twiceNums;
}




//TESTS - do not change these

console.log('######### Test for arrayTimesTwo([1, 2, 1, 3, 3]): ' + (arrayTimesTwo([1, 2, 1, 3, 3]).join(',') === [2,4,2,6,6].join(',')));
