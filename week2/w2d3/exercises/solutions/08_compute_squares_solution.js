/*
  Write a function that given an array, returns another array with each of the numbers squared.
  Don't change the original array, make sure you make a new array and return that new array.
*/

function computeSquares(nums){
  var squareNums = [];
  for(var i = 0; i < nums.length; i++) {
      squareNums.push(nums[i] * nums[i]);
  }
  return squareNums;
}




